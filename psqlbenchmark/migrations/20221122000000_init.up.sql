CREATE TABLE IF NOT EXISTS uuid_model (id UUID PRIMARY KEY DEFAULT gen_random_uuid());
CREATE TABLE IF NOT EXISTS identity_model (
  id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  type VARCHAR(16) NOT NULL
);
CREATE TABLE IF NOT EXISTS bigserial_model (id BIGSERIAL, type VARCHAR(16) NOT NULL);
CREATE TABLE IF NOT EXISTS timestamped_model (
  created_at TIMESTAMPTZ NOT NULL DEFAULT current_timestamp
);
CREATE TABLE IF NOT EXISTS nanosecond_model (
  created_at_seconds BIGINT NOT NULL,
  created_at_nanos BIGINT NOT NULL
);
CREATE INDEX IF NOT EXISTS idx_uuid_model_id ON uuid_model USING btree(id);
CREATE INDEX IF NOT EXISTS idx_bigserial_model_id ON bigserial_model USING btree(id);
CREATE INDEX IF NOT EXISTS idx_bigserial_model_type ON bigserial_model USING hash(type);
CREATE INDEX IF NOT EXISTS idx_identity_model_id ON identity_model USING btree(id);
CREATE INDEX IF NOT EXISTS idx_identity_model_type ON identity_model USING hash(type);
CREATE INDEX IF NOT EXISTS idx_timestamped_model_created_at ON timestamped_model USING btree(created_at);
CREATE INDEX IF NOT EXISTS idx_nanosecond_model_created_at ON nanosecond_model USING btree(created_at_seconds, created_at_nanos);
